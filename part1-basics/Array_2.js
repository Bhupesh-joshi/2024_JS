// Array 
// Push Pop
// Array shift Unshift


// push
let fruits = ["apple", "orange", "grapes"];

fruits.push("Mango");
console.log(fruits);


// pop
let arr = ["a", "b", "c"];
console.log(arr);
let poppedItem = arr.pop();

console.log(arr);
console.log("Popped Item is -> " + poppedItem);


// unshift
let color = ["black", "white"];
console.log(color);

color.unshift("green", "yellow");
console.log(color);


// Shift
let box = ["pen", "copy", "books"];
console.log(box);

let shiftedFromBox = box.shift();

console.log(box);
console.log(shiftedFromBox);

