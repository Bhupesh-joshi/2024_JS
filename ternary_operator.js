// Ternary Operator

let age = 88;
let drink;

if (age >= 10) {
    console.log(drink = "cold Drink");
} else {
    console.log(drink = "milk");
}

// second way
if (age >= 10) {
    drink = "please - Cold drink";
} else {
    drink = "please - Hot Milk";
}
console.log(drink);



// Ternary Operator ** Conditional operator

let myAge = 8;
let myDrink = myAge >= 10 ? "coffee" : "water";
console.log(myDrink);

